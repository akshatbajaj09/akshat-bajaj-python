# Some common string methods.
sample_string =  'why so serious?'
print(sample_string)                           #
print(sample_string.capitalize())              #
print(sample_string)                           #
print(sample_string.upper())                   # The original string remains the same.
print(sample_string)                           #
print(sample_string.lower())                   #
print(sample_string)                           #

sample_string = sample_string.capitalize()     #
print(sample_string)                           #    
sample_string = sample_string.upper()          # A new string is created every time.
print(sample_string)                           #
sample_string = sample_string.lower()          #
print(sample_string)                           #
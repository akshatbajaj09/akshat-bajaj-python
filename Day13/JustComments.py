# It took a lot of time to clear the confusion between encapsulation and abstraction but
# now here is the gist, encapsulation is simply data protection meaning restricting the
# access to some variables that we don't want to be modified
# and abstraction is abstracting or hiding unnecessary details like for example we are using a 
# TV remote to change the channels but we don't need to know how the signal is coded for it, 
# similarly the user of our code doesn't need to know the logic behind our code and hence using
# abstraction helps us to prevent the user from doing unnecessary modifications and also 
# it improves the simplicity of our code.